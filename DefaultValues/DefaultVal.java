// Prgram to print default values of primitive datatypes built in java
class DefaultVal{
	public static void main(String[] args){
	
	System.out.println("Minimum Value of Int type: " + Integer.MIN_VALUE);
	System.out.println("Maximum Value of Int type: " + Integer.MAX_VALUE);
	
	System.out.println("Minimum Value of Byte type: " + Byte.MIN_VALUE);
	System.out.println("Maximum Value of Byte type: " + Byte.MAX_VALUE);
	
	System.out.println("Minimum Value of Double type: " + Double.MIN_VALUE);
	System.out.println("Maximum Value of Double type: " + Double.MAX_VALUE);
	
	System.out.println("Minimum Value of Float type: " + Float.MIN_VALUE);
	System.out.println("Maximum Value of Float type: " + Float.MAX_VALUE);
	
	System.out.println("Minimum Value of Long type: " + Long.MIN_VALUE);
	System.out.println("Maximum Value of Long type: " + Long.MAX_VALUE);
	
	System.out.println("Minimum Value of Short type: " + Short.MIN_VALUE);
	System.out.println("Maximum Value of Short type: " + Short.MAX_VALUE);
	}
}